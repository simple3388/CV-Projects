using UnityEngine;

// static provider for two fixed 12x12 level layouts.
// Legend:
// '.' = ground only
// '#' = wall
// 'F' = fire
// 'K' = Keystone
// 'W' = WaterPot
// 'E' = Exit
// 'G' = Keystone destination goal
//Note player will always spawn at bottom row column 7
public static class LevelDefinitions
{
    private const int Size = 12;


    //layout for level 1
    private static readonly char[,] layout1 = new char[Size, Size]
    {
        // Row 0 (BOTTOM)
        {'#','#','#','#','#','#','.', '#','#','#','#','#'},
        {'#','.','.','.','.','.','.','.','.','.','.','#'},
        {'#','.','.','.','.','.','E','.','.','.','.','#'},
        {'#','.','.','.','.','.','.','.','.','.','.','#'},
        {'#','.','.','.','G','.','.','.','.','.','.','#'},
        {'#','.','.','.','.','.','.','.','.','.','.','#'},
        {'#','.','.','#','#','#','F','#','#','#','.','#'},
        {'#','.','.','W','.','.','.','.','.','K','.','#'},
        {'#','.','.','#','#','#','.','#','#','#','.','#'},
        {'#','.','.','F','.','.','.','.','F','.','.','#'},
        {'#','.','.','.','.','.','.','.','.','.','.','#'},
        {'#','#','#','#','#','#','#','#','#','#','#','#'}
    };


    //layout for level 2
    private static readonly char[,] layout2 = new char[Size, Size]
    {
        // Row 0 (BOTTOM)
        {'#','#','#','#','#','#','.', '#','#','#','#','#'},
        {'#','.','.','.','.','.','.','.','.','.','.','#'},
        {'#','.','.','.','.','G','.','E','.','.','.','#'},
        {'#','#','#','.','#','#','#','#','#','.','#','#'},
        {'#','.','.','.','W','.','.','.','.','.','.','#'},
        {'#','.','#','#','#','#','.','.','.','.','.','#'},
        {'#','.','#','.','.','K','.','.','#','.','.','#'},
        {'#','.','#','.','F','F','F','.','#','.','.','#'},
        {'#','.','#','.','.','.','.','.','#','.','.','#'},
        {'#','.','#','#','#','#','#','#','#','.','.','#'},
        {'#','.','.','.','.','F','.','.','.','.','.','#'},
        {'#','#','#','#','#','#','#','#','#','#','#','#'}
    };

    public static char[,] GetLayout(int levelIndex)
    {
        if (levelIndex == 1) return (char[,])layout1.Clone();
        return (char[,])layout2.Clone();
    }
}
